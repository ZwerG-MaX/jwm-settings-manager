int MouseUI::parseline(char *line, char **argv, int maxarg) {
  int argc=0;
  while (*line!=0 && argc<maxarg) {      
    while (*line==' ' || *line=='\t' || *line =='\n') *line++=0;     
    *argv++ = line;
    argc++;
    while (*line!=0 && *line!=' ' && *line!='\t' && *line!='\n') line++;             
  }
  *argv = 0;
  return argc;
}

